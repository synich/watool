void* linit(){
  void *L = NULL;
#ifdef SUPPORT_LUA
  L = (lua_State *)lua_open();  /* create state */
  if (L == NULL) {
    puts("cannot create lua_State: not enough memory");
  } else {
    L = (void*)lua_open();
    lua_gc(L, LUA_GCSTOP, 0);  /* stop collector during initialization */
    luaL_openlibs(L);  /* open libraries */
    luaopen_utf8(L);
    luaopen_enc(L);
    lua_gc(L, LUA_GCRESTART, 0);
    lua_pop(L, 2);  // pop custom open_lib
    luafn_s(L);
    /* luaL_dostring(L, s_lua_precode); */
  }
#endif
  return L;
}

int ldofile(void* L, char *fname, int narg){
  int status = 0;
#ifdef SUPPORT_LUA
  status = luaL_loadfile(L, fname);
  if (0!=status) {
    printf("load %s fail[%d]: %s %s\n", fname, status, lua_tostring(L, -2), lua_tostring(L, -1));
    lua_pop(L, 2);
    return status;
  }
  if (0<narg) {lua_insert(L, 1);}
  status = lua_pcall(L, narg, LUA_MULTRET, 0);
  if (0!=status) {
    printf("run %s fail[%d]: %s %s\n",fname, status, lua_tostring(L, -2), lua_tostring(L, -1));
    lua_pop(L, 2);
  } else {
    lua_pop(L, 1);
  }
#endif
  return status; /*0-ok 1-fail*/
}

void lclose(void* L){
#ifdef SUPPORT_LUA
  lua_close((lua_State *)L);
#endif
}

void run_lua(int argc, char** argv){
  void *L = linit();
#ifdef SUPPORT_LUA
  if (2==argc) {
    char fname[MAXLINE];
    get_exe_path(fname);
    strcat(fname, "init.lua");
    ldofile(L, fname, 0);
  } else {
    int narr = argc - 3, i;
    lua_createtable(L, narr, 0);
    for (i=3; i < argc; i++) {
      lua_pushstring(L, argv[i]);
      lua_rawseti(L, -2, i-2);
    }
    lua_setglobal(L, "arg");
    ldofile(L, argv[2], 0);
  }
  lclose(L);
#endif
}


void enc_lua(int argc, char *argv[])
{
  char buf[128] = {0};
  char* pos; int i = 1, ret;
  char fndecl[128] = {0};
  FILE *fw, *fr;

  if (argc <2){
    usage();
  }

  sprintf(buf, "luac -s %s", argv[1]); /*drop debug*/
  ret = system(buf);
  if (0!=ret){puts("luac fail");return;}

  sprintf(buf, "%s.c", argv[1]);
  pos = strchr(buf, '.');
  *pos = '_';
  fr = fopen("luac.out", "rb");
  fw = fopen(buf, "w");

  *pos = 0; // let buf be the lua module name
  sprintf(fndecl, "#include \"lua/lauxlib.h\"\n\n"
    "static void luafn_%s(lua_State* L) {\n"
    "  const unsigned char B1[]={\n    ", buf);
  fwrite(fndecl, strlen(fndecl), 1, fw);

  while(1){
    unsigned char ch;
    char* comma = ",";  int cnt;
    char* nline = "\n    ";
    char fch[4] = {0};
    cnt = fread(&ch, 1, 1, fr);
    if (cnt == 0) break;
    sprintf(fch, "%u", ch); // convert to string
    fwrite(&fch, strlen(fch), 1, fw);
    fwrite(comma, 1, 1, fw);
    if ( i++%16 == 0) fwrite(nline, 5, 1, fw);
    //if (feof(fr)) break;
  }

  sprintf(fndecl, "\n  };\n\n  if (luaL_loadbuffer"
    "(L,(const char*)B1,sizeof(B1),\"%s\")==0)\n"
    "    lua_call(L, 0, 1);\n"
    "  lua_setglobal(L, \"%s\");\n"
    "}\n", buf, buf);
  fwrite(fndecl, strlen(fndecl), 1, fw);
  fclose(fr);
  fclose(fw);
}


